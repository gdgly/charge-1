package com.iycharge.server.domain.entity.notification;

import java.util.Date;

public class MailTemplate {
	 private String name;
	    private String invokeName;
	    private String templateType;
	    private String templateStat;
	    private Date gmtCreated;
	    private Date gmtUpdated;
	    
	    
		public MailTemplate() {
			super();
		}
		public MailTemplate(String name, String invokeName, String templateType, String templateStat, Date gmtCreated,
				Date gmtUpdated) {
			super();
			this.name = name;
			this.invokeName = invokeName;
			this.templateType = templateType;
			this.templateStat = templateStat;
			this.gmtCreated = gmtCreated;
			this.gmtUpdated = gmtUpdated;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getInvokeName() {
			return invokeName;
		}
		public void setInvokeName(String invokeName) {
			this.invokeName = invokeName;
		}
		public String getTemplateType() {
			return templateType;
		}
		public void setTemplateType(String templateType) {
			this.templateType = templateType;
		}
		public String getTemplateStat() {
			return templateStat;
		}
		public void setTemplateStat(String templateStat) {
			this.templateStat = templateStat;
		}
		public Date getGmtCreated() {
			return gmtCreated;
		}
		public void setGmtCreated(Date gmtCreated) {
			this.gmtCreated = gmtCreated;
		}
		public Date getGmtUpdated() {
			return gmtUpdated;
		}
		public void setGmtUpdated(Date gmtUpdated) {
			this.gmtUpdated = gmtUpdated;
		}
	    
}
