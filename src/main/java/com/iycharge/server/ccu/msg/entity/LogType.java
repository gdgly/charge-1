package com.iycharge.server.ccu.msg.entity;

/**
 * 日志类型
 * @author bwang
 */
public enum LogType {
    
    ONLINE("上线"), OFFLINE("下线");
    
    private String title;
    
    private LogType(String title) {
        this.title = title;
    }

    public String getTitle() {
        return title;
    }
}
