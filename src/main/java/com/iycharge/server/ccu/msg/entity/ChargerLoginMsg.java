package com.iycharge.server.ccu.msg.entity;

/**
 * 充电桩登录辨识报文
 * @author bwang
 */
public class ChargerLoginMsg extends BaseEntity {
   
    /*
     * 充电桩编号
     */
    private String chargerNo;
    
    /*
     *  协议版本号
     */
    private String protocolVersion;
    
    /*
     * 主控制器 软件版本号 
     */
    private String majorVersion;
    
    /*
     * 充电桩类型
     */
    private String chargerType;
    
    /*
     * 电表个数
     */
    private int meterNum;
    
    public ChargerLoginMsg() {
        
    }

    public String getChargerNo() {
        return chargerNo;
    }

    public void setChargerNo(String chargerNo) {
        this.chargerNo = chargerNo;
    }

    public String getProtocolVersion() {
        return protocolVersion;
    }

    public void setProtocolVersion(String protocolVersion) {
        this.protocolVersion = protocolVersion;
    }

    public String getMajorVersion() {
        return majorVersion;
    }

    public void setMajorVersion(String majorVersion) {
        this.majorVersion = majorVersion;
    }

    public String getChargerType() {
        return chargerType;
    }

    public void setChargerType(String chargerType) {
        this.chargerType = chargerType;
    }

    public int getMeterNum() {
        return meterNum;
    }

    public void setMeterNum(int meterNum) {
        this.meterNum = meterNum;
    }

    @Override
    public String toString() {
        return "ChargerLoginMsg [chargerNo=" + chargerNo + ", protocolVersion=" + protocolVersion + ", majorVersion="
                + majorVersion + ", chargerType=" + chargerType + ", meterNum=" + meterNum + "]";
    }
}
