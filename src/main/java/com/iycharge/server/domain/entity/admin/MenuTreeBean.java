package com.iycharge.server.domain.entity.admin;

import java.util.ArrayList;
import java.util.List;

/**
 * 菜单实体类
 * @author zw
 */
public class MenuTreeBean{
    
    private String text;
    private String href;
    private String tags;
    private List<MenuTreeBean> nodes = new ArrayList<MenuTreeBean>(); 
    private StateBean state = new StateBean();
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getHref() {
		return href;
	}
	public void setHref(String href) {
		this.href = href;
	}
	public String getTags() {
		return tags;
	}
	public void setTags(String tags) {
		this.tags = tags;
	}
	public List<MenuTreeBean> getNodes() {
		return nodes;
	}
	public void setNodes(List<MenuTreeBean> nodes) {
		this.nodes = nodes;
	}
	public StateBean getState() {
		return state;
	}
	public void setState(StateBean state) {
		this.state = state;
	}
	   
    
}
